#include "libft.h"

void	ft_putstr_fd(char *s, int fd)
{
	size_t	i;
	char	c;

	if (!s)
		return ;
	i = 0;
	while (i != ft_strlen(s))
	{
		c = s[i];
		write(fd, &c, 1);
		i++;
	}
}
